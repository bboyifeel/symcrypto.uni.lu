delta_in = 0x01
delta_out = 0x06


S = [[14,  4, 13,  1,  2, 15, 11,  8,  3, 10,  6, 12,  5,  9,  0,  7],
	 [ 0, 15,  7,  4, 14,  2, 13,  1, 10,  6, 12, 11,  9,  5,  3,  8],
	 [ 4,  1, 14,  8, 13,  6,  2, 11, 15, 12,  9,  7,  3, 10,  5,  0],
	 [15, 12,  8,  2,  4,  9,  1,  7,  5, 11,  3, 14, 10,  0,  6, 13]]


def fi(x, k):
	x ^= k

	y = (x & int('011110',2)) >> 1
	r = (x & int('100000',2)) >> 4 ^ (x & 1)

	return S[r][y]

